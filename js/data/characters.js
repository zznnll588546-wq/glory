const _allCharacters = [...CORE_CHARACTERS, ...CHARACTER_SUPPLEMENTS];
const _byId = new Map();
for (const c of _allCharacters) {
  const prev = _byId.get(c.id);
  if (!prev) {
    _byId.set(c.id, c);
  } else {
    _byId.set(c.id, { ...prev, ...c });
  }
}
export const CHARACTERS = [..._byId.values()];

export const CHARACTER_MAP = {};
for (const c of CHARACTERS) CHARACTER_MAP[c.id] = c;

export function getCharacter(id) { return CHARACTER_MAP[id]; }
export function searchCharacters(query) {
  const q = query.toLowerCase();
  return CHARACTERS.filter(c =>
    c.name.includes(q) || c.realName.includes(q) ||
    c.accountCard.includes(q) ||
    (c.aliases || []).some(a => a.toLowerCase().includes(q))
  );
}
export function getCharactersByTeam(teamId, season) {
  return CHARACTERS.filter(c => {
    const state = c.timelineStates[season];
    return state && state.team === teamId;
  });
}
