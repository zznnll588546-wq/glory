export function getWorldBooksBySeason(season) {
  return WORLD_BOOKS.filter(wb =>
    wb.season === 'all' || wb.season.split(',').includes(season)
  );
}

export function getWorldBooksByCategory(category) {
  return WORLD_BOOKS.filter(wb => wb.category === category);
}
